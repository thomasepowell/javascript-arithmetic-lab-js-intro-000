function add(a, b) {

  return a+b
}

function subtract(a, b) {
  return a-b
}

function multiply(a, b) {
  return a*b
}

function divide(a, b) {
  return a/b
}

function inc(n) {
  var mynum = n
  mynum++
  return mynum
}

function dec(n) {
  var mynum = n
  mynum--
  return mynum
}
function makeInt(n) {
  var mynum = (parseint(n, 10))
  return mynum
}
